#烤红薯案例：定义一个红薯的类，具有烤的功能，参数有时间和红薯的状态

class sweetpotato():
    def __init__(self):
        self.c_time=0
        self.state='生的'
    def __str__(self):
        return f'红薯目前烤了{self.c_time}分钟,目前是{self.state}'
    def cook(self,time):#烤的功能实现的是，可以设定时间，并且红薯的状态随着烤的时间而变化
        self.c_time=self.c_time+time
        if self.c_time<10:
            self.state='生的'
        elif 10<=self.c_time<20:
            self.state='半熟'
        elif 20<=self.c_time<30:
            self.state='熟了'
        elif self.c_time>30:
            self.state='糊了'
            
        
        
        
        
