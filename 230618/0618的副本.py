#设计一个小游戏，文字类型，包含游戏角色的创建，游戏内容的设计（打怪升级，闯关获得装备）
#用面向对象的方法设计游戏：【游戏本体】和【游戏角色】都属于对象，
#1⃣创建角色的类，然后用类创建出具体的游戏角色
import random

class character():
    def __init__(self,job,name):
        self.level=1
        self.job=job
        self.name=name
        self.equip=[]
    def __str__(self):
        return f'角色名:{self.name}\n职业:{self.job}\n等级:{self.level}\n装备:{self.equip}'

class Monster():
    def __init__(self, name):
        self.num=random.randint(1,5)
        self.name=name
        
    
#2⃣创建游戏角色的类，然后创建出具体的游戏   
class game():
    def __init__(self):
        self.hit_num=0
        self.equip_list=['黄金甲','轩辕剑','金箍棒','踏云靴','穿云箭']
        
    def hit(self,char, monster):
        self.hit_num+=monster.num
        if self.hit_num>=10:
            char.level+=10
            self.hit_num-=10
            
        print(f'{char.name}消灭一个{monster.name},得到{monster.num}分,level:{char.level}')
            
    def hit_boss(self,char):
        if char.level<20:
            print('等级不足，无法挑战boss，快去升级吧！')
        else:
            print(f'{char.name}击杀了boss')
            char.equip.append(self.equip_list[random.randint(0,4)])

#创建一个游戏            
wz=game()
#让用户输入名字
name=input('请输入名字:')

#让用户选择职业,必须选择列表中的项
while 1:
    print('请输入职业[战士,法师,刺客,射手,辅助]')
    job=input('请选择角色:')
    if job == '战士' or job == '法师' or job == '刺客' or job == '射手'or job == '辅助':
        break;
    else:
        print("输入的职业错误，请重新输入!")
        
    

#创建角色
kkk=character(job,name)
print('角色创建成功')
print(kkk)


while 1:
    print('[1]开始打怪[2]挑战boss[3]查看角色[4]退出游戏')
    num=int(input('请选择操序号:'))
    if num==1:
        wz.hit(kkk, Monster('小怪兽'))
    elif num==2:
        wz.hit_boss(kkk)
    elif num==3:
        print(kkk)
    elif num==4:
        print('你已成功退出游戏!')
        break










































































































































































































































































































































































































































































































































































































































































































































































































































































